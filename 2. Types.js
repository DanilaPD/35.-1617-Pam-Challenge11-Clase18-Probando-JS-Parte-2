// String:
let firstVariable = "I am a string.";
console.log(typeof firstVariable);

// Number:
firstVariable = 25;
console.log(typeof firstVariable);

// Number con decimales:
let secondVariable = 27.5;
console.log(typeof secondVariable);

// Booleans:
let itIsTrueBaby = true;
//Type
console.log(typeof itIsTrueBaby);
//True or False
console.log(itIsTrueBaby);

// Array aunque lo va a tomar como un object:
let fruitsBaby = ["banana", "orange", "pear", 29, false];
console.log(typeof fruitsBaby);

// Object:
let humanParts = {
    eyes: 2,
    legs: 2,
    head: 1,
    tail: false,
    name: "terminator",
};
//Type
console.log(typeof humanParts);
//Content
console.log(humanParts);